"""Function to declare the Type of Parameters.

This functions are used to declare the parameters of the view functions.
By annotating the return value with Any, we make sure that the code editor
don't complain too much about assigning a default value of type ViewParameter
to a parameter that have been annotated with a pydantic-compatible type...

Credits: This module is essentially a fork from the params module of FlaskAPI.
"""

from typing import Any
from typing import Dict
from typing import Optional

from pydantic.fields import Undefined

from .parameters import BodyParameter
from .parameters import CookieParameter
from .parameters import FileParameter
from .parameters import FormParameter
from .parameters import HeaderParameter
from .parameters import ParamLocation
from .parameters import PathParameter
from .parameters import QueryParameter


def Path(  # noqa:N802
    default: Any = Undefined,
    *,
    alias: Optional[str] = None,
    title: Optional[str] = None,
    description: Optional[str] = None,
    gt: Optional[float] = None,
    ge: Optional[float] = None,
    lt: Optional[float] = None,
    le: Optional[float] = None,
    min_length: Optional[int] = None,
    max_length: Optional[int] = None,
    regex: Optional[str] = None,
    example: Any = Undefined,
    examples: Optional[Dict[str, Any]] = None,
    deprecated: Optional[bool] = None,
    include_in_schema: bool = True,
    **extra: Any,
) -> Any:
    """Declare A Path parameter."""
    return PathParameter(
        default=default,
        alias=alias,
        title=title,
        description=description,
        gt=gt,
        ge=ge,
        lt=lt,
        le=le,
        min_length=min_length,
        max_length=max_length,
        regex=regex,
        example=example,
        examples=examples,
        deprecated=deprecated,
        include_in_schema=include_in_schema,
        **extra,
    )


